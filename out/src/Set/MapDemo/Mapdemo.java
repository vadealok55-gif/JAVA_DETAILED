import java.util.*;
public class Mapdemo {
    public static void main(String[] args) {
        //map doesn't extend Collection interface
        // key - value pairs (each entry must consists of key and value)
        //UNIQUE keys , One value per Key

        HashMap<Integer ,String > map=new HashMap<>();

        map.put(1,"alok");
        map.put(2,"vade");
        map.put(3,"vadealok");

        System.out.println(map);
        // output :{1=alok, 2=vade, 3=vadealok}

        String name=map.get(3);
        System.out.println(name);
        // output : vadealok

        boolean b = map.containsKey(3);
        System.out.println(b);

        System.out.println(map.containsValue("vade"));

        Set<Integer> keys=map.keySet();
        for(int n:keys){
            System.out.println(map.get(n)+" ");
        }


        for(int  n: map.keySet()){
            System.out.print(map.get(n));
        }

        for(Map.Entry<Integer,String>  entry:map.entrySet()){
            System.out.println(entry);
        }

        for(Map.Entry<Integer,String>  entry:map.entrySet()){
           entry.setValue(entry.getValue().toUpperCase());
        }
        System.out.println(map);//all values in upper case using set

        String r= map.remove(2);
       boolean res= map.remove(1,"aloka");// returns false

        System.out.println(r +" "+ res);
        //no order
        // key can contain only  one NULL
        // value can contain many NULL values
        // not thread safe


        // methods take O( 1 ) time complexity

        //Internal structure of Map and its time complexity o(1)

        // basic components
        /*

        value , key , bucket , hashFunction


     ->   buckets are nothing but internal array (think buckets as cells in list)
     ->   Hashfunction generates hashcode using key and  using hashcode stored and accessed
     ->   this hashcode can be generated by hashfunction using a key everytime gives same hashcode or hashvalue
     ->   how does it work  put function work
              step 1: key is passed through hashfunction to generate UNIQUE hashcode
                      hashcode(number integer) helps to stored in array(called "bucket")
              step 2: int index = HashCode % array_size;
                      index decides which bucket will hold this key value pair
              step 3: storing


            Collision Detection:
       */

    }
}
